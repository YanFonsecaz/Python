def celsius_para_fahrenheit(celsius = 0):
    
    return(celsius * 9/5) + 32
print(celsius_para_fahrenheit(0))
print(celsius_para_fahrenheit(100))

def metros_para_centimetros(metros = 0):
    return metros * 100
print(metros_para_centimetros(1.5))

def segundos_para_minutos(segundos = 0):
    return segundos / 60
print(segundos_para_minutos(120))
