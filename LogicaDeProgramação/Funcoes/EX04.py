def area_retangulo(largura, altura):
    area = largura * altura
    return area
print(area_retangulo(5,3))

def area_circulo(raio):
    area = 3.14159 * raio ** 2
    return area
print(area_circulo(2))

def area_triangulo(base, altura):
    area = (base * altura) / 2
    return area
print(area_triangulo(6, 4))
